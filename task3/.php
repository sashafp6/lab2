<?php

function isAscending($n) { // Проверяет, возрастают ли цифры числа
    $s = strval($n); // Преобразует число в строку
    for ($i = 1; $i < strlen($s); $i++) { // Перебирает цифры строки
        if ($s[$i] <= $s[$i - 1]) { // Если текущая цифра не больше предыдущей
            return false; // Не возрастающее
        }
    }
    return true; // Возрастающее
}

function reverseNumber($n) { // Переворачивает число
    $s = strval($n); // Преобразует число в строку
    return intval(strrev($s)); // Переворачивает строку и преобразует обратно в число
}

$numbers = [4, 87, 129, 33, 45]; // Массив чисел
echo "Result2: "; // Выводит строку "Результат: "
foreach ($numbers as $num) { // Перебирает массив чисел
    if (isAscending($num)) { // Если число возрастающее
        echo reverseNumber($num) . " "; // Выводит перевернутое число и пробел
    }
}
echo "\n"; // Выводит символ новой строки

?>
